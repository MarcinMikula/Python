# Przykład 1: Podstawowe operacje matematyczne
x = 10          # Przypisanie wartości 10 do zmiennej x
y = 3           # Przypisanie wartości 3 do zmiennej y
suma = x + y    # Dodawanie
iloczyn = x * y # Mnożenie
print(suma)     # Wyświetla 13
print(iloczyn)  # Wyświetla 30

# Przykład 2: Typy danych i operatory
a = 5.5         # Liczba zmiennoprzecinkowa (float)
b = 2           # Liczba całkowita (int)
c = a / b       # Dzielenie
d = b ** 2      # Potęgowanie
tekst = "Witaj" # Ciąg znaków (string)
print(c)        # Wyświetla 2.75
print(d)        # Wyświetla 4
print(tekst + " świecie!")  # Wyświetla "Witaj świecie!"

# Przykład 3: Zmienne i zmiana wartości
x = 7           # Początkowa wartość x
x = x + 1       # Zwiększenie wartości x o 1
print(x)        # Wyświetla 8